public class OperatorCopy {
    public static void main(String[] args){
        System.out.println("=====BELAJAR OPERATOR=====");
        System.out.println(" ");
        int x = 100;
        double y = 120;
        double hasil = x / y;
        System.out.println("Total adalah " + hasil);
        x = x + 1;
        System.out.println("Nilai x adalah " + x);
        ++x;
        System.out.println("Nilai x adalah " + x);
        x++;
        System.out.println("Nilai x adalah " + x);
        System.out.println("Nilai x adalah " + --x);
        System.out.println("Nilai x adalah " + x++);
        System.out.println("Nilai x adalah " + x);
        x = x + 7;
        System.out.println("Nilai x adalah " + x);
        x += 5;
        System.out.println("Nilai x adalah " + x);
    }
}
